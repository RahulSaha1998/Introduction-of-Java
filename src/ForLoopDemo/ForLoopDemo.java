package ForLoopDemo;

public class ForLoopDemo {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Hello Bro");
        }
        for (int i = 5; i >= 1; i--) {
            System.out.println(i + " Hello Bro");
        }
        for (int i = 1; i <= 50; i = i + 2) {
            System.out.println(i);
        }

        int sum = 0;
        for (int i = 1; i <= 5; i++) {
            sum = sum + i;
            System.out.println(sum);
        }
    }
}

package AssignmentOperator;

public class AssignmentOperator {
    public static void main(String[] args) {
        int x = 3;
        int y = 2;

        x += y;
        System.out.println("X = " + x);
        x -= y;
        System.out.println("X = " + x);
        x *= y;
        System.out.println("X = " + x); 
        x /= y;
        System.out.println("X = " + x); 
        x %= y;
        System.out.println("X = " + x); 
    }

}
